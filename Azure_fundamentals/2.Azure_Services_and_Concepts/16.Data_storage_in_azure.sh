Modern Applications require data to be available quickly and stored securely and accessbile from all over the world.

Azure provides Storage services for the specific data types

Benefits to Azure data storage solutions
  1. Automated backup and recovery
  2. Replication across the world
  3. Encryptiomn options
  4. security and platform integration


Categories of Data:
--------------------->
1. Structured Data(SQL - Relational)
   -> Azure SQL Database
   -> Azure Database for MySQL
   -> Azure Database for PostgreSQL 

2. UnStructured Data
   -> Azure Blob storage
   -> Azure File storage 
   -> Azure Disk Storage 

2. SemiStructured Data(NoSQL)
   -> Cosmos DB 
    


